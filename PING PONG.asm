; DRAWING LEFT PADDLE
  MOV CX,PADDLE_LEFT_X; X starting position
  MOV DX,PADDLE_LEFT_Y; Y starting position

  LOOP_DRAW_PADDLE_LEFT_HORIZONTAL:
    MOV AH,0CH ;WRITE GRAPHICS PIXEL
    MOV BH,0H  ; PAGE NUMBER
    INT 10H
    INC CX
    MOV BX,CX
    SUB BX,PADDLE_LEFT_X
    CMP BX,PADDLE_WIDTH
    JNG LOOP_DRAW_PADDLE_LEFT_HORIZONTAL

  MOV CX,PADDLE_LEFT_X; moving to  next line by moving to initial position
  INC DX ; move one step down
  MOV BX,DX
  SUB BX,PADDLE_LEFT_Y
  CMP BX,PADDLE_HEIGHT
  JNG LOOP_DRAW_PADDLE_LEFT_HORIZONTAL



; DRAWING RIGHT PADDLE
 MOV CX,PADDLE_RIGHT_X; X starting position
  MOV DX,PADDLE_RIGHT_Y; Y starting position

  LOOP_DRAW_PADDLE_RIGHT_HORIZONTAL:
    MOV AH,0CH ;WRITE GRAPHICS PIXEL
    MOV BH,0H  ; PAGE NUMBER
    INT 10H
    INC CX
    MOV BX,CX
    SUB BX,PADDLE_RIGHT_X
    CMP BX,PADDLE_WIDTH
    JNG LOOP_DRAW_PADDLE_RIGHT_HORIZONTAL

  MOV CX,PADDLE_RIGHT_X; moving to  next line by moving to initial position
  INC DX ; move one step down
  MOV BX,DX
  SUB BX,PADDLE_RIGHT_Y
  CMP BX,PADDLE_HEIGHT
  JNG LOOP_DRAW_PADDLE_RIGHT_HORIZONTAL





RET
DRAW_PADDLES ENDP
;--------------------------------------
MOV_PADDLES PROC NEAR
; CHECK IF ANY KEY IS BEING PRESSED
 MOV AH,01H   
 INT 16H
 JZ CHECK_RIGHT_PADDLE  ; IF ZF=1 

; CHECK WHICH KEY IS PRESSED
 MOV AH,00h   
 INT 16H





; FOR MULTI PLAYER MODE

 ; W,w FOR LEFT PADDLE UP
 CMP AL,77H ;w
 JE LEFT_PADDLE_UP
 CMP AL,57H ;W
 JE LEFT_PADDLE_UP

 ;S,s FOR left PADDLE down
 CMP AL,73H ;w
 JE LEFT_PADDLE_DOWN
 CMP AL,53H ;W
 JE LEFT_PADDLE_DOWN
 JMP CHECK_RIGHT_PADDLE


  


 LEFT_PADDLE_UP:
   MOV AX,PADDLE_VELOCITY
   SUB PADDLE_LEFT_Y,AX
   
   MOV AX,WINDOW_BOUNDARIES
   CMP PADDLE_LEFT_Y,AX
   JL FIX_PADDLE_LEFT_TOP


   JMP CHECK_RIGHT_PADDLE

   FIX_PADDLE_LEFT_TOP:
     MOV AX,WINDOW_BOUNDARIES
     MOV PADDLE_LEFT_Y,AX
     JMP CHECK_RIGHT_PADDLE


 LEFT_PADDLE_DOWN:
   MOV AX,PADDLE_VELOCITY
   ADD PADDLE_LEFT_Y,AX
   MOV AX ,WINDOW_HEIGHT
   SUB AX,PADDLE_HEIGHT
   CMP PADDLE_LEFT_Y,AX
   JG FIX_PADDLE_LEFT_BOTTOM
   JMP CHECK_RIGHT_PADDLE

   FIX_PADDLE_LEFT_BOTTOM:
     MOV PADDLE_LEFT_Y,AX
     JMP CHECK_RIGHT_PADDLE



CHECK_RIGHT_PADDLE:

 CMP AI_LEFT_PADDLE_CONTROLLED,01H
 JE AI_USED
 
KEYS_CHECKING:
 ; O,O FOR LEFT PADDLE UP
 CMP AL,50H ;P
 JE RIGHT_PADDLE_UP
 CMP AL,70H ;p
 JE RIGHT_PADDLE_UP

 ;L,l FOR left PADDLE down
 CMP AL,4CH ;L
 JE RIGHT_PADDLE_DOWN
 CMP AL,6CH ;l
 JE RIGHT_PADDLE_DOWN

 JMP EXIT_PADDLE

 ; FOR SINGLE PLAYER MODE
 AI_USED:
   MOV AX,BALL_Y
   ADD AX,BALL_SIZE
   CMP AX,PADDLE_RIGHT_Y
   JL RIGHT_PADDLE_UP
   MOV AX,PADDLE_RIGHT_Y
   ADD AX,PADDLE_HEIGHT
   CMP AX,BALL_Y
   JL RIGHT_PADDLE_DOWN
   JMP EXIT_PADDLE



 RIGHT_PADDLE_UP:
   MOV AX,PADDLE_VELOCITY
   SUB PADDLE_RIGHT_Y,AX
   
   MOV AX,WINDOW_BOUNDARIES
   CMP PADDLE_RIGHT_Y,AX
   JL FIX_PADDLE_RIGHT_TOP


   JMP EXIT_PADDLE

   FIX_PADDLE_RIGHT_TOP:
     MOV AX,WINDOW_BOUNDARIES
     MOV PADDLE_RIGHT_Y,AX
     JMP EXIT_PADDLE


 RIGHT_PADDLE_DOWN:
   MOV AX,PADDLE_VELOCITY
   ADD PADDLE_RIGHT_Y,AX
   MOV AX ,WINDOW_HEIGHT
   SUB AX,PADDLE_HEIGHT
   CMP PADDLE_RIGHT_Y,AX
   JG FIX_PADDLE_RIGHT_BOTTOM
   JMP EXIT_PADDLE

   FIX_PADDLE_RIGHT_BOTTOM:
     MOV PADDLE_RIGHT_Y,AX
     JMP EXIT_PADDLE





EXIT_PADDLE:
 RET
MOV_PADDLES ENDP
