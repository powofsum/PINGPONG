 ;CONVERT DECIMAL TO ASCII
 ;ADD 30H DECIMAL TO ASCII
 ADD AL,30H
 MOV [TEXT_LEFT_PLAYER_POINTS],AL




 RET
UPDATE_TEXT_POINTS_LEFT_PLAYER ENDP
;---------------------------------------
