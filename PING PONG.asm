TEXT_MAIN_MENU_SINGLE_PLAYER DB 'SINGLE PLAYER-S','$'
TEXT_MAIN_MENU_MULTI_PLAYER DB 'MULTI PLAYER-M','$'

CURRENT_SCENE DB 0 ;0:MAIN MENU 1:GAME

AI_LEFT_PADDLE_CONTROLLED DB 0





DATA ENDS

CODE SEGMENT PARA 'CODE'

  MAIN PROC FAR
  ASSUME CS:CODE, DS:DATA ,SS:STACK
  PUSH DS
  XOR AX,AX
  PUSH AX
  MOV AX,DATA ; initialize data segment
  MOV DS,AX

  MOV AH,00h ; graphics mode
  MOV AL,13h
  INT 10h

  MOV AH,0Bh ;background colour
  MOV BH,00h
  MOV BL,00h 
  INT 10h

CHECK_TIME:

 CMP CURRENT_SCENE,00h
 JE SHOW_MAIN_MENU

 CMP GAME_ACTIVE,00h
 JE SHOW_GAME_OVER

 MOV AH,2CH ; FOR TIME CH=HOURS CL=MINUTES DH=SECONDS 
 INT 21H 
 CMP DL,TIME_PREV ; WE ARE COMPARING 8 BITS THAT'S WHY TIME_PRIV IS DB
 JE CHECK_TIME
 MOV TIME_PREV,DL ; UPDATE TIME
 




 MOV AL,00H    ; removing pixel by changing its colour to black
 CALL DRAW_PADDLES
 CALL MOV_PADDLES
 MOV AL,0FH
 CALL DRAW_PADDLES

 MOV AL,00H    ; removing pixel by changing its colour to black
 CALL DRAW_BALL
 CALL MOV_BALL
 MOV AL,0FH    ; COLOUR IS WHITE
 CALL DRAW_BALL 

 CALL USER_INTERFACE

 

JMP CHECK_TIME

SHOW_GAME_OVER:
 CALL DRAW_GAME_OVER_UI
 JMP CHECK_TIME
 

SHOW_MAIN_MENU:
 CALL DRAW_MAIN_MENU 
 JMP CHECK_TIME






 MOV AH,00h ; add wait
 INT 16H 



  MOV AX,4C00H ; TERMINATE
  INT 21H
  MAIN ENDP


