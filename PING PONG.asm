DRAW_GAME_OVER_UI PROC NEAR


  
  MOV AH,0Bh ;background colour
  MOV BH,00h
  MOV BL,00h 
  INT 10h

  ;WRITE
  MOV AH,02H  ;CURSOR POSITION
  MOV BH,00h  ;PAGE NUMBER
  MOV DH,0CH  ;ROW
  MOV DL,37H  ;COLUMN
  INT 10h

  MOV AH,09H   ;WRITE  STRING
  LEA DX,TEXT_GAME_OVER ; DX POINTER TO STRING
  INT 21H ;PRINTS

  ;-------SHOW WINNER-------

  ;WRITE
  MOV AH,02H  ;CURSOR POSITION
  MOV BH,00h  ;PAGE NUMBER
  MOV DH,0EH  ;ROW
  MOV DL,34H  ;COLUMN
  INT 10h

  CALL UPDATE_WINNER_TEXT

  MOV AH,09H   ;WRITE  STRING
  LEA DX,TEXT_GAME_OVER_WINNER ; DX POINTER TO STRING
  INT 21H ;PRINTS

  ;----DISPLAY MAIN MENU TEXT IN GAME OVER----
  
  ;WRITE
  MOV AH,02H  ;CURSOR POSITION
  MOV BH,00h  ;PAGE NUMBER
  MOV DH,1EH  ;ROW
  MOV DL,50H  ;COLUMN
  INT 10h

  MOV AH,09H   ;WRITE  STRING
  LEA DX,TEXT_GAME_OVER_MAIN_MENU ; DX POINTER TO STRING
  INT 21H ;PRINTS

  ; ----DISPLAY PLAY AGAIN TEXT----
  ;WRITE
  MOV AH,02H  ;CURSOR POSITION
  MOV BH,00h  ;PAGE NUMBER
  MOV DH,2FH  ;ROW
  MOV DL,50H  ;COLUMN
  INT 10h

  MOV AH,09H   ;WRITE  STRING
  LEA DX,TEXT_GAME_OVER_PLAY_AGAIN ; DX POINTER TO STRING
  INT 21H ;PRINTS


 ; Wait for a key press
   MOV AH,00H     
  INT 16H 

; ESC KEY TO PLAY AGAIN 
  CMP AL,1BH
  JE RESTART_GAME

; E,e EXIT TO MAIN MENU  
  CMP AL,'E'
  JE EXIT_TO_MAIN_MENU
  CMP AL,'e'
  JE EXIT_TO_MAIN_MENU
  RET


EXIT_TO_MAIN_MENU:
  MOV GAME_ACTIVE,00h
  MOV CURRENT_SCENE,00h
  RET 

 RESTART_GAME:
  CALL CLEAR_SCREEN
  MOV GAME_ACTIVE,01H
  RET
DRAW_GAME_OVER_UI ENDP 
