 XOR AX,AX
 MOV AL,PADDLE_RIGHT_PLAYER_PONTS

 ;CONVERT DECIMAL TO ASCII
 ;ADD 30H DECIMAL TO ASCII
 ADD AL,30H
 MOV [TEXT_RIGHT_PLAYER_POINTS],AL

 RET
UPDATE_TEXT_POINTS_RIGHT_PLAYER ENDP
