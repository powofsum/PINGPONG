STACK SEGMENT PARA STACK
  DB 64 DUP (' ')
STACK ENDS

DATA SEGMENT PARA 'DATA'

BALL_X DW 0A0H
BALL_Y DW 64H
BALL_SIZE DW 04H
TIME_PREV DB 0H ; TO CHECK PREVIOUS VALUE OF TIME (make sure it has changed)
VELOCITY_X DW 05H
VELOCITY_Y DW 02H
WINDOW_WIDTH DW 140H ;320 PIXELS
WINDOW_HEIGHT DW 0C8H ;200 PIXELS
WINDOW_BOUNDARIES DW 6  ; without this boundaries the pixel will appear in random place during collosion then 
                        ; disappear instantly,this is beacuse we are not checking where was the last position our ball was
                        ; so we have to make some boundaries to prevent this from happening
BALL_ORG_X DW 0A0H
BALL_ORG_Y DW 64H

PADDLE_LEFT_X DW 06H
PADDLE_LEFT_Y DW 02H

PADDLE_RIGHT_X DW 135H
PADDLE_RIGHT_Y DW 02H

PADDLE_WIDTH DW 05H
PADDLE_HEIGHT DW 1FH
PADDLE_VELOCITY DW 05H

PADDLE_LEFT_PLAYER_PONTS DB 0
