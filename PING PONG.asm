 DRAW_BALL  PROC NEAR
  
  MOV CX,BALL_X ; X starting position
  MOV DX,BALL_Y ; Y starting position

  LOOP_DRAW_BALL_HORIZONTAL:
  MOV AH,0CH ;WRITE GRAPHICS PIXEL
  MOV BH,0H  ; PAGE NUMBER
  INT 10H
  INC CX
  MOV BX,CX
  SUB BX,BALL_X
  CMP BX,BALL_SIZE
  JNG LOOP_DRAW_BALL_HORIZONTAL
  
  MOV CX,BALL_X ; moving to  next line by moving to initial position
  INC DX ; move one step down
  MOV BX,DX
  SUB BX,BALL_Y
  CMP BX,BALL_SIZE
  JNG LOOP_DRAW_BALL_HORIZONTAL

  RET
 DRAW_BALL ENDP 
;-----------------------------------------
 RESET_POS PROC NEAR
   MOV AX ,BALL_ORG_X
   MOV BALL_X,AX

   MOV AX ,BALL_ORG_Y
   MOV BALL_Y,AX
  
  RET
  RESET_POS ENDP
;----------------------------------------
  MOV_BALL PROC NEAR

  MOV AX,VELOCITY_X
  MOV BX,VELOCITY_Y
  ADD BALL_X,AX   ; MOVE BALL X AXIS
  
  MOV AX ,WINDOW_BOUNDARIES
  CMP BALL_X,AX
  ; JL RESET_POS_CALL
  JL GIVE_POINTS_LEFT_PLAYER

  MOV AX,WINDOW_WIDTH
  SUB AX,WINDOW_BOUNDARIES
  CMP BALL_X,AX
  ; JG  RESET_POS_CALL
  JG GIVE_POINTS_RIGHT_PLAYER
  JMP MOV_BALL_IN_Y_AXIS


  GIVE_POINTS_LEFT_PLAYER:
   INC PADDLE_RIGHT_PLAYER_PONTS
  ;  CALL RESET_POS
   CALL UPDATE_TEXT_POINTS_RIGHT_PLAYER

    NEG VELOCITY_X      ; Bounce back
    MOV AX,WINDOW_BOUNDARIES
    MOV BALL_X,AX 

   CMP PADDLE_RIGHT_PLAYER_PONTS,5H
   JG GAME_OVER
   RET


   GIVE_POINTS_RIGHT_PLAYER:
    INC PADDLE_LEFT_PLAYER_PONTS
    ; CALL RESET_POS
    CALL UPDATE_TEXT_POINTS_LEFT_PLAYER

    NEG VELOCITY_X      ; Bounce back
    MOV AX,WINDOW_WIDTH
    SUB AX,WINDOW_BOUNDARIES
    SUB AX,BALL_SIZE
    MOV BALL_X,AX

    CMP PADDLE_LEFT_PLAYER_PONTS,5H
    JG GAME_OVER
    RET


   GAME_OVER:   ; AFTER REACHING 5 PONTS RESET GAME
    CMP PADDLE_LEFT_PLAYER_PONTS,5H ; FIRST CHECK THE WINNER 
    JNL WINNER_IS_LEFT_PLAYER
    JMP WINNER_IS_RIGHT_PLAYER
     
     WINNER_IS_LEFT_PLAYER:
      MOV WINNER_INDEX,01H ;  MEANS PLAYER 1
      JMP CONTINUE

     WINNER_IS_RIGHT_PLAYER:
      MOV WINNER_INDEX,02H  ; INDEX 1 MEANS PLAYER 2
      
   CONTINUE:
    MOV PADDLE_LEFT_PLAYER_PONTS,00H
    MOV PADDLE_RIGHT_PLAYER_PONTS,00H
    CALL UPDATE_TEXT_POINTS_LEFT_PLAYER
    CALL UPDATE_TEXT_POINTS_RIGHT_PLAYER 
    MOV GAME_ACTIVE,00H
    RET

  RESET_POS_CALL:
     CALL RESET_POS
     RET

 MOV_BALL_IN_Y_AXIS:
  ADD BALL_Y,BX   ; MOVE BALL Y AXIS

  MOV AX,WINDOW_BOUNDARIES 
  CMP BALL_Y,AX
  JL NEG_VELOCITY_Y

  MOV AX,WINDOW_HEIGHT
  SUB AX,WINDOW_BOUNDARIES  
  CMP BALL_Y,AX            ; boundaries checked with ball y axis position and window height
  JG NEG_VELOCITY_Y

;-------COLLOSION-------
;CHECK IF BALL COLLIDES WITH RIGHT PADDLE
; AABB COLLOSION
; BALL_X+BALL_SIZE > PADDLE_RIGHT_X   &&  BALL_X < PADDLE_RIGHT_X + PADDLE_WIDTH
; BALL_Y+BALL_SIZE> PADDLE_RIGHT_Y    &&  BALL_Y < PADDLE_RIGHT_Y + PADDLE_HEIGHT


MOV AX,BALL_X
ADD AX,BALL_SIZE
CMP AX,PADDLE_RIGHT_X
JNG CHECK_LEFT_COLLOSION ;NO COLLOSION,CHECK LEFT PADDLE

MOV AX,PADDLE_RIGHT_X
ADD AX,PADDLE_WIDTH
CMP BALL_X,AX
JNL CHECK_LEFT_COLLOSION

MOV AX,BALL_Y
ADD AX,BALL_SIZE
CMP AX,PADDLE_RIGHT_Y
JNG CHECK_LEFT_COLLOSION

MOV AX,PADDLE_RIGHT_Y
ADD AX,PADDLE_HEIGHT
CMP BALL_Y,AX
JNL CHECK_LEFT_COLLOSION


; THERE IS COLLOSION DETECTED WITH RIGHT PADDLE
NEG VELOCITY_X
RET ; EXIT AS BALL WON'T COLLIDE WITH 2 PADELS AT SAME TIME






;CHECK IF BALL COLLIDES WITH LEFT PADDLE
; BALL_X+BALL_SIZE > PADDLE_LEFT_X   &&  BALL_X < PADDLE_LEFT_X + PADDLE_WIDTH
; BALL_Y+BALL_SIZE> PADDLE_LEFT_Y    &&  BALL_Y < PADDLE_LEFT_Y + PADDLE_HEIGHT
CHECK_LEFT_COLLOSION:

MOV AX,BALL_X
ADD AX,BALL_SIZE
CMP AX,PADDLE_LEFT_X
JNG EXIT_COLLOSION ;NO COLLOSION

MOV AX,PADDLE_LEFT_X
ADD AX,PADDLE_WIDTH
CMP BALL_X,AX
JNL EXIT_COLLOSION

MOV AX,BALL_Y
ADD AX,BALL_SIZE
CMP AX,PADDLE_LEFT_Y
JNG EXIT_COLLOSION

MOV AX,PADDLE_LEFT_Y
ADD AX,PADDLE_HEIGHT
CMP BALL_Y,AX
JNL EXIT_COLLOSION

NEG VELOCITY_X
RET ;


NEG_VELOCITY_Y:
    NEG VELOCITY_Y
    RET


EXIT_COLLOSION:
   RET

  MOV_BALL ENDP
